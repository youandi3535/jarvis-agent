def get(agent_id: str) -> str:
    """에이전트 ID로 페르소나 반환. 미등록 시 빈 문자열."""
    return PERSONAS.get(agent_id, "")


def with_system(agent_id: str, existing_system: str = "") -> str:
    """페르소나 + 기존 system 합성. existing_system 은 뒤에 붙음."""
    persona = get(agent_id)
    if not persona:
        return existing_system
    if not existing_system:
        return persona
    return persona + "\n\n" + existing_system


